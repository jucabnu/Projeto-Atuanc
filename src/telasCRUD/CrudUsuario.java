    private void botaoGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGravarActionPerformed

        Usuario u = new Usuario();
        u.setNomusu("");

        try {
            if (campoCodigo.getText().equals("")) {
                // nada
                System.out.println("fez nada");
            } else {                
                u = DAOUsuario.getUsuarioPeloCod(Integer.parseInt(campoCodigo.getText()));
                System.out.println("pegou usuario pelo codigo");
            }
            
            if (u.getNomusu().equals("")) {
                
                System.out.println("entrou em gravar");
                
                // GRAVAR : INSERT
                
                Usuario usu = new Usuario();
                usu.setNomusu(campoNome.getText());
                usu.setEmausu(campoEmail.getText());
                usu.setLogusu(campoLogin.getText());
                usu.setPasusu(jPasswordField.getText());
                int tipo = 2;                
                if(String.valueOf(jComboBoxTipo.getSelectedItem()).equals("Administrador")){                
                    tipo = 1;
                }
                usu.setTipusu(tipo);
                
                if(DAOUsuario.duplicado(campoLogin.getText(), campoEmail.getText())){
                    JOptionPane.showMessageDialog(null, "Login/e-mail em uso! Escolha outro.");
                }else{
                    DAOUsuario.inserir(usu);                
                    JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!");
                }               
                
            } else {
                
                // ALTERAR : UPDATE
                System.out.println("entrou em atualizar");
                
                Usuario usu = new Usuario();
                usu.setCodusu(Integer.parseInt(campoCodigo.getText()));
                usu.setNomusu(campoNome.getText());
                usu.setEmausu(campoEmail.getText());
                usu.setLogusu(campoLogin.getText());
                usu.setPasusu(jPasswordField.getText());
                int tipo = 2;                
                if(String.valueOf(jComboBoxTipo.getSelectedItem()).equals("Administrador")){
                    tipo = 1;
                }
                System.out.println(String.valueOf(jComboBoxTipo.getSelectedItem()));
                usu.setTipusu(tipo);
                
                if(DAOUsuario.duplicado(campoLogin.getText(), campoEmail.getText())){
                    JOptionPane.showMessageDialog(null, "Login/e-mail em uso! Escolha outro.");
                }else{
                    DAOUsuario.atualizar(usu);                
                    JOptionPane.showMessageDialog(null, "As alterações foram gravadas com sucesso!");
                }    
                
            }
        } catch (NumberFormatException | HeadlessException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Formato inválido informado em algum dos campos ou"
                    + "\n" + "campo obrigatório deixado em branco."
                    + "\n" + "Favor reiniciar o cadastro."
                    + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
        
        

    }//GEN-LAST:event_botaoGravarActionPerformed

