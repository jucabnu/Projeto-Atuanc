    private void campoCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoCodigoFocusLost

        // MOSTRAR CLIENTE : SELECT
        try {
            if (campoCodigo.getText().equals("")) {
                botaoLimparActionPerformed(null);
            } else {
                
                Usuario u = DAOUsuario.getUsuarioPeloCod(Integer.parseInt(campoCodigo.getText()));
                
                campoNome.setText(u.getNomusu());
                campoEmail.setText(u.getEmausu());
                campoLogin.setText(u.getLogusu());
                jPasswordField.setText(u.getPasusu());
                
                if(u.getTipusu() == 1){
                    jComboBoxTipo.setSelectedIndex(1);                      
                }else{
                    jComboBoxTipo.setSelectedIndex(0);
                }
                
            }
        } catch (NumberFormatException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Código inválido!"
                    + "\n"
                    + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoCodigoFocusLost

