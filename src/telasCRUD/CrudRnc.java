    private void campoCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoCodigoFocusLost

        // MOSTRAR RNC : SELECT (aparentemente tudo ok)
        try {
            if (campoCodigo.getText().equals("")) {
                // nada
            } else {
                
                Rnc n = DAORnc.getRncPeloNum(Integer.parseInt(campoCodigo.getText()));
                
                //jLabelDataFinal.setText(String.valueOf(n.getDatfim()));
                if(n.getDatfim() != null){
                    jLabelDataFinal.setText(Datas.converteData(n.getDatfim()));
                }else{
                    jLabelDataFinal.setText("-");
                }
                jLabelDataInicio.setText(Datas.converteData(n.getDatini()));                
                jLabelDataRegistro.setText(Datas.converteData(n.getDatreg()));        
                jLabelUltimaAlteracao.setText(Datas.converteData(n.getDatalt()));                
                
                jLabelUsuarioResp.setText(DAOUsuario.getUsuarioPeloCod(n.getCodusu()).getLogusu());
                jLabelUsuarioUltimaAlt.setText(DAOUsuario.getUsuarioPeloCod(n.getAltusu()).getLogusu());
                jTextAreaIdeCausa.setText(n.getIdecau());
                jTextAreaRespostaFinal.setText(n.getResfim());
                jTextFieldAreaResp.setText(n.getResind());
                jTextFieldClienteFornecedor.setText(n.getClifor());
                jTextFieldDisposicao.setText(n.getDisind());
                jTextFieldFal.setText(n.getNumfal());
                jTextFieldNotaFiscal.setText(String.valueOf(n.getNumnfe()));
                jTextFieldOrigem.setText(n.getOricau());
                jTextFieldProdutoServico.setText(n.getProser());
                jTextFieldQuantidade.setText(String.valueOf(n.getQtdrnc()));
                jTextFieldResponsavel.setText(n.getResind());
                jTextFieldValor.setText(String.valueOf(n.getValind()));
                jComboBoxSituacao.setSelectedIndex(n.getSituacao());
                
                if(n.isLabind()){
                    jComboBoxLaboratorio.setSelectedIndex(0);
                }else{
                    jComboBoxLaboratorio.setSelectedIndex(1);
                }
                jComboBoxMotivo.setSelectedItem(DAOMotivo.getMotivoPeloCod(n.getCodmot()).getDesmot());
                jComboBoxSetor.setSelectedItem(DAOSetor.getSetorPeloCod(n.getCodset()).getNomset());
                
                
                // travar edição para RNC fechada se usuário não é ADM
                if(travar(n, user)){
                    travarEdicao();
                }
                                
            }
        } catch (NumberFormatException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Código inválido!"
                    + "\n"
                    + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoCodigoFocusLost

