                if(String.valueOf(jComboBoxLaboratorio.getSelectedItem()).equals("Sim")){
                    lab = true;
                }                
                rnc.setLabind(lab);
                                                
                rnc.setIdecau(jTextAreaIdeCausa.getText());
                rnc.setOricau(jTextFieldOrigem.getText());
                rnc.setClifor(jTextFieldClienteFornecedor.getText());
                rnc.setProser(jTextFieldProdutoServico.getText());
