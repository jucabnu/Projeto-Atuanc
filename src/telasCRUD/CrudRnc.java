    private void iniComb(){

        // SIT
        
        jComboBoxSituacao.removeAllItems();
        jComboBoxSituacao.addItem("Em andamento"); //0
        jComboBoxSituacao.addItem("Procedente");   //1
        jComboBoxSituacao.addItem("Improcedente"); //2
        
        // LAB
        jComboBoxLaboratorio.removeAllItems();
        jComboBoxLaboratorio.addItem("Sim");
        jComboBoxLaboratorio.addItem("Não");
        
        // SETOR
        jComboBoxSetor.removeAllItems();
        ArrayList<Setor> setores = DAOSetor.getSetores();
        setores.forEach((s) -> {
            jComboBoxSetor.addItem(s.getNomset());
        }); 
        
        //MOTIVO
        jComboBoxMotivo.removeAllItems();
        ArrayList<Motivo> motivos = DAOMotivo.getMotivos();
        motivos.forEach((m) -> {
            jComboBoxMotivo.addItem(m.getDesmot());
        });
        
        
    }

